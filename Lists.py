#Lists - Keep track of data easily
#store data and access later
family = ["Tangeni", "Linda", "Bonny"]
#index:       0         1        2
#print
print(family)
#print with only one element:
print(family[1])
print(family[-1])
#adding ":" to the end of a number shows all elements after
print(family[1:])
#specify range:
print(family[0:3])
#change the value in an list
family[1] = "Pandeni"
print(family[1])

